    private DienThoaiModel read() {
        DienThoaiModel dt = new DienThoaiModel();
        dt.setBatteryCapacity(txtBatteryCapacity.getText());
        dt.setBrand(txtBrand.getText());
        dt.setColor(txtColor.getText());
        dt.setMenmory(txtMemory.getText());
        dt.setModelName(txtModelName.getText());
        dt.setPrice(Double.parseDouble(txtPrice.getText() + ""));
        dt.setQuantity(Integer.valueOf(txtQuantity.getText() + ""));
        dt.setStatus(chkStatus.isSelected() ? "Còn Hàng" : "Hết Hàng");
        return dt;
    }

