    private void FilltoTable() {
        model.setRowCount(0);
        int i = 1;
        for (DienThoaiModel dt : msv.lstdt) {
            model.addRow(new Object[]{
                i++,
                dt.getModelName(),
                dt.getPrice(),
                dt.getBrand(),
                dt.getColor(),
                dt.getMenmory(),
                dt.getBatteryCapacity(),
                dt.getQuantity(),
                dt.getStatus()
            });
        }
    }

    private void showdetail() {
        DienThoaiModel dt = msv.lstdt.get(index);
        txtModelName.setText(dt.getModelName());
        txtBrand.setText(dt.getBrand());
        txtColor.setText(dt.getColor());
        txtPrice.setText(dt.getPrice() + "");
        txtMemory.setText(dt.getMenmory());
        txtQuantity.setText(dt.getQuantity() + "");
        txtBatteryCapacity.setText(dt.getBatteryCapacity());
        chkStatus.setSelected(dt.getStatus().equalsIgnoreCase("Còn Hàng") ? true : false);
        tblDienThoai.setRowSelectionInterval(index, index);

    }

